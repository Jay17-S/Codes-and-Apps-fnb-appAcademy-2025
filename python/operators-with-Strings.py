# OPERATORTS WITH STRINGS

# STRINGS CANT BE CONCANTINATED WITH INTs OR FLOATs
# YOU NEED TO TURN YOUR INTs or FLOATs TO STRINGS IN ORDER TO MAKE THEM WORK

str1 = "hello"
str2 = "world"

print ( str1,'',str2) # this also combines strings
print ( str1,"", str2, "", str2) # You can do it multiple times
print ( str1 + str2)
