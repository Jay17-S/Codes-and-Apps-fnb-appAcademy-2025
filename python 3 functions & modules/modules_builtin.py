# importing some built in modules in python and see how to use them
# math module imports mathematical functions
import math

print(math.pi)

# Random module - can generate random numbers

import random

print(random.randint(0, 123))

# datetime module - allows us to manipulate dates and time

import datetime
print(datetime.datetime.now()) # this is how you use it

# OS module - this provides a way of using operating system depending functions

import os
print(os.getcwd()) #shows where this file is loccated

# sys module - 

import sys
print(sys.version) #shows the python version installed in system
