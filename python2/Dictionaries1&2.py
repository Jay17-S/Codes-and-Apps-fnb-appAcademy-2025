# DICTIONARIES 1
# A dictionary is a collection of key value pairs, unlike lists or tuples which are indexed by a range or numbers.
# dictionaries are indexed by key, which can be of any type, can be a string or numeric data type.

# You can define a dictionary by using curly brackets {}

my_dict = {"apple" :3, "banana" :5, "oranges" :8}
print(my_dict["apple"]) # this will show they key/ value of apple which is 3

# ADDING KEY PAIRS----------

my_dict["grapes"] = 4 # this adds this pair
print(my_dict)

# MODIFYING A PAIR----------------

my_dict["banana"] = 7
print(my_dict)

